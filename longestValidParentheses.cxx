using namespace std;

class Solution
{
public:
	int longestValidParentheses(string s)
	{
		int max_par = 0;
		stack<int> stk;
		stk.push(-1);
		for (int i=0; i<s.length(); i++) {
			if (s[i] == '(') {
				stk.push(i);
			} else {
				stk.pop();
				if (stk.empty()) // invalid, start again
					stk.push(i);
				else
					max_par = max(max_par, i - stk.top());
			}
		}
		return max_par;
	}
};

